    private void receber(){
        if(    jTextFieldIdLivro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo!");
        } else if(!jTextFieldIdLivro.getText().matches("\\d*")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Id Livro apenas com caracteres num√©ros!");
        } else {
            int idLivro = Integer.parseInt(jTextFieldIdLivro.getText());
            DAOEmprestimo daoEmp = new DAOEmprestimo();
            Emprestimo emprestimo = daoEmp.localizar(idLivro);
            daoEmp.remover(emprestimo);
            DAOLivro.receberLivro(idLivro);
            JOptionPane.showMessageDialog(this, "Livro recebido com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            limparCampos();
            TelaOpcoesFunc tela = new TelaOpcoesFunc();
            tela.setVisible(true);
            dispose();
        }
    }
    
