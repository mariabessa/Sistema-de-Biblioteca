    public void tabela(){
        String columns[] = {"id", "Nome", "Disponível"};
        DAOLivro daoLivro = new DAOLivro();
        List<Livro> livros = daoLivro.getList();
        String[][] data = livros.stream()
    .map(livro -> new String[]{livro.getId().toString(), livro.getTitulo(), livro.isDisponivel()})
    .toArray(String[][]::new);
        DefaultTableModel model = new DefaultTableModel(data, columns);
        jTable1.setModel(model);
    }
