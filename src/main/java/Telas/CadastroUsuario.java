    private void limparCampos() {
        jTextFieldNome.setText("");
        jTextFieldSobrenome.setText("");
        jTextFieldReg.setText("");
        jTextFieldSenha.setText("");
    }
    private void adicionarUsuario() {
        if(    jTextFieldNome.getText().isEmpty() || jTextFieldSobrenome.getText().isEmpty()
            || jTextFieldReg.getText().isEmpty() || jTextFieldSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        } else if(!jTextFieldReg.getText().matches("\\d*")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Registro Academico apenas com caracteres num√©ros!");
        } else {
            String nome = jTextFieldNome.getText();
            String sobrenome = jTextFieldSobrenome.getText();
            int reg = Integer.parseInt(jTextFieldReg.getText());
            String senha = jTextFieldSenha.getText();
            DAOUsuario daousu = new DAOUsuario();
            // Criar o objeto Funcionario com os dados fornecidos
            Usuario usuario = new Usuario(nome, sobrenome, senha, reg);
            daousu.incluir(usuario);
            JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            TelaUsuarios telaAnterior = new TelaUsuarios();
            telaAnterior.setVisible(true);
            dispose();
        }
    }
    
