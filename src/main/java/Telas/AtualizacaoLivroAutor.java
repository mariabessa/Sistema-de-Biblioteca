    private void gerarAutores() {
    JPanel panel = new JPanel(new GridLayout(0, 1));

    for (Autor autor : Dados.listaAutores) {
        JCheckBox checkBox = new JCheckBox(autor.getNome() + " " + autor.getSobreNome());

        checkBox.addActionListener(e -> {
            if (checkBox.isSelected()) {
                autoresSelecionados.add(autor);
            } else {
                autoresSelecionados.remove(autor);
            }
        });

        panel.add(checkBox);
    }

    getContentPane().add(panel, new org.netbeans.lib.awtextra.AbsoluteConstraints(60, 320, 300, -1));


    }

    
