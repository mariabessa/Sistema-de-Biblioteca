    private void atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarActionPerformed
    if(    jTextFieldSobrenome.getText().isEmpty() || jTextFieldNome.getText().isEmpty()
        || jTextFieldMatricula.getText().isEmpty() || jTextFieldSenha.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
    } else if(!jTextFieldMatricula.getText().matches("\\d*")) {
        JOptionPane.showMessageDialog(null, "Preencha o campo Matrícula apenas com caracteres numéros!");
    } else {
        String sobrenome = jTextFieldSobrenome.getText();
        String nome = jTextFieldNome.getText();
        int matricula = Integer.parseInt(jTextFieldMatricula.getText());
        String senha = jTextFieldSenha.getText();
        DAOFuncionário daoFuncionario = new DAOFuncionário();
        Funcionario funcionario = new Funcionario(nome, sobrenome, matricula, senha);
        funcionario.setId(ContextoLivro.getIdAtual());
        daoFuncionario.atualizar(funcionario);
        FuncionarioEspecifico telaFuncionario = new FuncionarioEspecifico(funcionario);
        telaFuncionario.setVisible(true);
        dispose();
    }
    }//GEN-LAST:event_atualizarActionPerformed

