    private void gerarCategorias() {
        JPanel panel = new JPanel(new GridLayout(0, 1));

        for (Categoria cat : Dados.listaCategorias) {
            JCheckBox checkBox = new JCheckBox(cat.getTitulo());
            checkBoxes.add(checkBox); // Adiciona Ã  lista de checkboxes
            panel.add(checkBox);

            checkBox.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    if (checkBox.isSelected()) {
                        categoriasSelecionadas.add(cat); // Adiciona a categoria selecionada
                    } else {
                        categoriasSelecionadas.remove(cat); // Remove a categoria desselecionada
                    }
                }
            });
        }
             getContentPane().add(panel, new org.netbeans.lib.awtextra.AbsoluteConstraints(60, 320, 300, -1));
    }

    
